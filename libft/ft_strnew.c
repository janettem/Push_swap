#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char *)malloc(sizeof(char) * (size + 1))))
		return ((char *)NULL);
	ft_bzero(str, size + 1);
	return (str);
}
